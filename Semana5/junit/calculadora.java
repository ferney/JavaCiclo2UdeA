public class calculadora {

    static int sumar(int a, int b, int c) {
   return a+b+c;
    }

    static int resta(int a, int b, int c) {
    return a-b-c;
    }

    static int multiplicacion(int a, int b, int c) {
   return a*b*c;
    }

    static double divicion(int a, int b, int c) {
        return (a/b)/c;//0.1666666
    }
    
}
